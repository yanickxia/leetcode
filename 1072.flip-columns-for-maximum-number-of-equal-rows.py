# @lc code=start
from typing import List


class Solution:
    def maxEqualRowsAfterFlips(self, matrix: List[List[int]]) -> int:
        m = len(matrix)
        if m == 0:
            return 0
        cache = {}
        for i in range(0, m):
            noraml = "".join([str(x) for x in matrix[i]])
            revese = "".join([str(1 - x) for x in matrix[i]])
            if noraml not in cache:
                cache[noraml] = 0
            cache[noraml] += 1
            if revese not in cache:
                cache[revese] = 0
            cache[revese] += 1
        return max([cache[x] for x in cache])


